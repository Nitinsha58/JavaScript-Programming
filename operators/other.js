// Other JavaScript Operators

/*
    Operator        Description
    ,               evaluates multiple operands and returns the value of the last operand.
    ?:              returns value based on the condition
    delete          deletes an objects's property, or an element of an array
    typeof          returns a string indicating the data type
    void            discards the expression's return value
    in              returns true if the specified property is in the object
    instanceof      returns true if the specified object is of the specified object type
*/